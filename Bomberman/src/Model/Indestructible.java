package Model;

public class Indestructible {
  private int xval, yval;
 
 public Indestructible() {
   int xval, yval = 0;
 }
 
 public int getXval(){
   return xval;
 }
 public int getYval(){
   return yval;
 }
 public void setXval(int i){
   xval = i;
 }
 public void setYval(int i){
   yval = i;
 }
}
